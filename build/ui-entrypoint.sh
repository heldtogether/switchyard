#!/bin/sh
set -e

API_BASE_URL=${UI_API_BASE_URL:-http://localhost:8080}
AUTH_LOGIN_URL=${UI_AUTH_LOGIN_URL:-}
AUTH_LOGOUT_URL=${UI_AUTH_LOGOUT_URL:-}
WORKSPACE_SLUG=${UI_WORKSPACE_SLUG:-default}
USE_MOCKS=${UI_USE_MOCKS:-false}
AGGREGATE_LIMIT=${UI_AGGREGATE_LIMIT:-5}
VERSION=${UI_VERSION:-${UI_BUILD_VERSION:-}}

cat > /usr/share/nginx/html/config.js <<CONFIG
window.__ENV = {
  API_BASE_URL: "${API_BASE_URL}",
  AUTH_LOGIN_URL: "${AUTH_LOGIN_URL}",
  AUTH_LOGOUT_URL: "${AUTH_LOGOUT_URL}",
  WORKSPACE_SLUG: "${WORKSPACE_SLUG}",
  USE_MOCKS: "${USE_MOCKS}",
  AGGREGATE_LIMIT: "${AGGREGATE_LIMIT}",
  VERSION: "${VERSION}"
};
CONFIG

exec nginx -g "daemon off;"
